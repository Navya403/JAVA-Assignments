import java.util.Scanner;

public class Whilefactorial {

	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		System.out.println("enter a number");
		int num=sc.nextInt();
		long fact=1;
		
		while(num<=num) {
			int i=0;
			fact*=i;
			i++;
		}

	}

}
